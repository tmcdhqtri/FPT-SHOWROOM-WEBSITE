    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String cusName = request.getParameter("NAME");
        String phone = request.getParameter("PHONE");
        String carID = request.getParameter("CARID");
        String dateTest = request.getParameter("DATETEST");
        String des = request.getParameter("DES");
        CarDAO dao = new CarDAO();
        if (dao.checkContainCar(carID)){
            request.setAttribute("MESSAGE1", "This car isn't in showroom car list");
            RequestDispatcher rd = request.getRequestDispatcher("RegisToDrive.jsp");
            rd.forward(request, response);
        }
        else{
            dao.addCarTester(cusName, phone, carID, dateTest, des);
            request.setAttribute("MESSAGE2", "Success");
            RequestDispatcher rd = request.getRequestDispatcher("RegisToDrive.jsp");
            rd.forward(request, response);
        }
    }


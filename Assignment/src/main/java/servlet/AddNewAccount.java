package servlet;

import dao.LoginDAO;
import jakarta.servlet.RequestDispatcher;
import java.io.IOException;
import java.io.PrintWriter;
import java.util.ArrayList;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import jakarta.servlet.http.HttpSession;
import java.util.List;
import java.util.logging.Level;
import java.util.logging.Logger;
import model.AdminInfo;
import model.CustomerInfo;

/**
 *
 * @author Admin
 */
@WebServlet(name = "AddNewAccount", urlPatterns = {"/AddNewAccount"})
public class AddNewAccount extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, Exception {
        response.setContentType("text/html;charset=UTF-8");
        HttpSession session = request.getSession();
        AdminInfo acc = (AdminInfo) session.getAttribute("ROLE");
        if (acc== null){
            response.sendRedirect("HomeServlet");
        } else{
        String url = "";

        String user = request.getParameter("USERNAME");
        String pass = request.getParameter("PASSWORD");
        String name = request.getParameter("CUSTOMERNAME");
        String phone = request.getParameter("PHONE");
        String email = request.getParameter("EMAIL");
        String address = request.getParameter("ADDRESS");
        String birthday = request.getParameter("BIRTHDAY");

        LoginDAO dao = new LoginDAO();
        CustomerInfo cus = dao.checkAccountExist(user);
        if (cus!=null) {
            request.setAttribute("MESSAGE", "Exist Username " + user);
            url = "AddNewAccount.jsp";
        } else {
            dao.addAccountCustomer(name, phone, email, address, birthday, user, pass);
            url = "AdminManageServlet";

        }
        RequestDispatcher rd = request.getRequestDispatcher(url);
        rd.forward(request, response);
        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            processRequest(request, response);
        } catch (Exception ex) {
            Logger.getLogger(AddNewAccount.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            processRequest(request, response);
        } catch (Exception ex) {
            Logger.getLogger(AddNewAccount.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
